public class ConversorUnidade {
    public static double gramasParaKg(double valor){
        return valor / 1000;
    }

    public static double gramasParaLibras(double valor){
        return valor * 0.0022;
    }

    public static double gramasParaOnças(double valor){
        return valor *  0.0353;
    }

    public static double kgParaGramas(double valor){
        return valor * 1000;
    }

    public static double kgParaLibras(double valor){
        return valor * 2.20462;
    }

    public static double kgParaOnças(double valor){
        return valor * 35.27396;
    }

    public static double librasParaGramas(double valor){
        return valor * 453.59;
    }

    public static double librasParaKg(double valor){
        return valor / 2.20462;
    }

    public static double librasParaOnças(double valor){
        return valor * 16;
    }

    public static double onçasParaGramas(double valor){
        return valor * 28.3495;
    }

    public static double onçasParaKg(double valor){
        return valor * 35.27396;
    }

    public static double onçasParaLibras(double valor){
        return valor / 16;
    }
}
